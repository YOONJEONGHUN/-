package 배열사용;

public class 기본형배열 {

	public static void main(String[] args) {
		
		int[] s = new int[10];
		for (int i = 0; i < s.length; i++) {
			System.out.print(s[i] + ", ");
		}
		System.out.println();
		for (int i = 0; i < s.length; i++) {
			s[i] = i;
		}
		for (int i = 0; i < s.length; i++) {
			System.out.print(s[i] + 1 + ", ");
		}
		}
	}


